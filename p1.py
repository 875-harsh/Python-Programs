# Input: Two complex numbers from the user
c1 = complex(input("Enter the first complex number (e.g., 1+2j): "))
c2 = complex(input("Enter the second complex number (e.g., 3+4j): "))

# Perform addition and subtraction
addition = c1 + c2
subtraction = c1 - c2

# Output the results
print(f"The addition of {c1} and {c2} is: {addition}")
print(f"The subtraction of {c1} and {c2} is: {subtraction}")
